package labDay1_myproject;

public class manualCast {
	public static void main(String[] args) {
		double d = 9.78;
		int a = (int)d;
		System.out.println("Double value: " + d);
		System.out.println("Integer value: "+ a); // converts double into int
		// Manual Casting --> Converts larger data type into smaller data type manually
	}
}
